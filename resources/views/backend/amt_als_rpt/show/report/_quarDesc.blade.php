<h1 class="text-center"><strong>感覺處理</strong></h1>
<blockquote>
    <p>意即孩童對於環境刺激感覺訊息的接收程度與行為處理模式，感覺閾值即是孩童的神經系統對於刺激的界線，界線過高而行為消極的孩童易出現精神渙散、專注力不集中；界線過高而行為消極的孩童則是過度亢奮、靜不下來；界線過低的孩子的神經系統一直處於備戰狀態，會出現排斥狀況甚至出現攻擊行為。</p>
</blockquote>
